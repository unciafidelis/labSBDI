const path = require("path");
const Database = require("better-sqlite3");

const dbPath = path.join(process.cwd(), "biblioteca.db");
const db = new Database(dbPath);

db.pragma("journal_mode = WAL");
db.pragma("foreign_keys = ON");

db.exec(`
  CREATE TABLE IF NOT EXISTS AUTOR (
  ID            INTEGER PRIMARY KEY,
  NOMBRE        TEXT NOT NULL,
  NACIONALIDAD  TEXT,
  BIBLIOGRAFIA  TEXT,
  CREATED_AT    TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT    TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);
`);
db.exec(`
  CREATE TABLE IF NOT EXISTS EDITORIAL (
  ID          INTEGER PRIMARY KEY,
  NOMBRE      TEXT NOT NULL,
  DIRECCION   TEXT,
  TELEFONO    TEXT,
  EMAIL       TEXT,
  CREATED_AT  TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT  TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (NOMBRE)
);
  `)

  db.exec(`
    CREATE TABLE IF NOT EXISTS LIBRO (
  ID                INTEGER PRIMARY KEY,
  TITULO            TEXT NOT NULL,
  ISBN              TEXT,
  GENERO            TEXT,
  IDIOMA            TEXT,
  PAGINAS           INTEGER CHECK (PAGINAS IS NULL OR PAGINAS > 0),
  FECHA_PUBLICACION TEXT,
  DESCRIPCION       TEXT,
  CREATED_AT        TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT        TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (ISBN)
);
  `)
  db.exec(`
  CREATE TABLE IF NOT EXISTS USUARIO (
  ID             INTEGER PRIMARY KEY,
  NOMBRE         TEXT NOT NULL,
  EMAIL          TEXT,
  TELEFONO       TEXT,
  DIRECCION      TEXT,
  TIPO           TEXT NOT NULL DEFAULT 'alumno'
    CHECK (TIPO IN ('alumno','docente','externo','bibliotecario','admin')),
  ACTIVO         INTEGER NOT NULL DEFAULT 1 CHECK (ACTIVO IN (0,1)),
  FECHA_REGISTRO TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT     TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (EMAIL)
);
  `)


db.exec(`
  CREATE TABLE IF NOT EXISTS LIBRO_AUTOR (
  LIBRO_ID       INTEGER NOT NULL,
  AUTOR_ID       INTEGER NOT NULL,
  ORDEN_AUTORIA  INTEGER CHECK (ORDEN_AUTORIA IS NULL OR ORDEN_AUTORIA > 0),
  ROL            TEXT,
  PRIMARY KEY (LIBRO_ID, AUTOR_ID),
  FOREIGN KEY (LIBRO_ID) REFERENCES LIBRO(ID)
    ON UPDATE CASCADE ON DELETE CASCADE,
  FOREIGN KEY (AUTOR_ID) REFERENCES AUTOR(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT
) WITHOUT ROWID;

CREATE INDEX IF NOT EXISTS idx_libro_autor_autor ON LIBRO_AUTOR (AUTOR_ID);
  `)

db.exec(`
  CREATE TABLE IF NOT EXISTS EDICION (
  ID                INTEGER PRIMARY KEY,
  LIBRO_ID          INTEGER NOT NULL,
  EDITORIAL_ID      INTEGER NOT NULL,
  NUM_EDICION       INTEGER CHECK (NUM_EDICION IS NULL OR NUM_EDICION > 0),
  FECHA_LANZAMIENTO TEXT,
  LUGAR_PUBLICACION TEXT,
  ISBN_EDICION      TEXT,
  CREATED_AT        TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UPDATED_AT        TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,

  FOREIGN KEY (LIBRO_ID) REFERENCES LIBRO(ID)
    ON UPDATE CASCADE ON DELETE CASCADE,
  FOREIGN KEY (EDITORIAL_ID) REFERENCES EDITORIAL(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT,

  UNIQUE (LIBRO_ID, EDITORIAL_ID, NUM_EDICION, FECHA_LANZAMIENTO),
  UNIQUE (ISBN_EDICION)
);

CREATE INDEX IF NOT EXISTS idx_edicion_libro ON EDICION (LIBRO_ID);
CREATE INDEX IF NOT EXISTS idx_edicion_editorial ON EDICION (EDITORIAL_ID);

CREATE TRIGGER IF NOT EXISTS trg_edicion_updated_at
AFTER UPDATE ON EDICION
FOR EACH ROW
BEGIN
  UPDATE EDICION
  SET UPDATED_AT = CURRENT_TIMESTAMP
  WHERE ID = NEW.ID;
END;
  `)
db.exec(`
  CREATE TABLE IF NOT EXISTS EJEMPLAR (
  ID            INTEGER PRIMARY KEY,
  EDICION_ID    INTEGER NOT NULL,
  CODIGO_BARRAS TEXT NOT NULL,
  UBICACION     TEXT,
  ESTADO        TEXT NOT NULL DEFAULT 'disponible'
    CHECK (ESTADO IN ('disponible','prestado','reservado','mantenimiento','baja')),
  FECHA_ALTA    TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,

  FOREIGN KEY (EDICION_ID) REFERENCES EDICION(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT,

  UNIQUE (CODIGO_BARRAS)
);

CREATE INDEX IF NOT EXISTS idx_ejemplar_edicion ON EJEMPLAR (EDICION_ID);
CREATE INDEX IF NOT EXISTS idx_ejemplar_estado  ON EJEMPLAR (ESTADO);
  `)

db.exec(`
  CREATE TABLE IF NOT EXISTS PRESTAMO (
  ID             INTEGER PRIMARY KEY,
  USUARIO_ID     INTEGER NOT NULL,
  FECHA_PRESTAMO TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  ESTADO         TEXT NOT NULL DEFAULT 'activo'
    CHECK (ESTADO IN ('activo', 'vencido','cerrado','cancelado')),
  OBSERVACIONES  TEXT,

  FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_prestamo_usuario ON PRESTAMO (USUARIO_ID);
CREATE INDEX IF NOT EXISTS idx_prestamo_estado  ON PRESTAMO (ESTADO);
  `)
db.exec(`
  CREATE TABLE IF NOT EXISTS PRESTAMO_ITEM (
  PRESTAMO_ID         INTEGER NOT NULL,
  EJEMPLAR_ID         INTEGER NOT NULL,
  FECHA_VENCIMIENTO   TEXT NOT NULL,
  FECHA_DEVOLUCION    TEXT,
  ESTADO              TEXT NOT NULL DEFAULT 'activo'
    CHECK (ESTADO IN ('activo','devuelto','vencido')),
  CONDICION_DEVOLUCION TEXT,
  MULTA_MXN            REAL NOT NULL DEFAULT 0 CHECK (MULTA_MXN >= 0),

  PRIMARY KEY (PRESTAMO_ID, EJEMPLAR_ID),

  FOREIGN KEY (PRESTAMO_ID) REFERENCES PRESTAMO(ID)
    ON UPDATE CASCADE ON DELETE CASCADE,
  FOREIGN KEY (EJEMPLAR_ID) REFERENCES EJEMPLAR(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT
) WITHOUT ROWID;

CREATE INDEX IF NOT EXISTS idx_prestamo_item_ejemplar ON PRESTAMO_ITEM (EJEMPLAR_ID);
CREATE INDEX IF NOT EXISTS idx_prestamo_item_estado   ON PRESTAMO_ITEM (ESTADO);

-- Evita 2 préstamos activos del mismo ejemplar
CREATE UNIQUE INDEX IF NOT EXISTS uq_ejemplar_prestamo_activo
ON PRESTAMO_ITEM (EJEMPLAR_ID)
WHERE ESTADO = 'activo';
  `)
db.exec(`
  CREATE TABLE IF NOT EXISTS RESERVA (
  ID            INTEGER PRIMARY KEY,
  USUARIO_ID    INTEGER NOT NULL,
  LIBRO_ID      INTEGER NOT NULL,
  FECHA_RESERVA TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  EXPIRA_EN     TEXT,
  ESTADO        TEXT NOT NULL DEFAULT 'activa'
    CHECK (ESTADO IN ('activa','cancelada','cumplida','expirada')),

  FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT,
  FOREIGN KEY (LIBRO_ID) REFERENCES LIBRO(ID)
    ON UPDATE CASCADE ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_reserva_usuario ON RESERVA (USUARIO_ID);
CREATE INDEX IF NOT EXISTS idx_reserva_libro   ON RESERVA (LIBRO_ID);
CREATE INDEX IF NOT EXISTS idx_reserva_estado  ON RESERVA (ESTADO);

CREATE UNIQUE INDEX IF NOT EXISTS uq_reserva_activa_usuario_libro
ON RESERVA (USUARIO_ID, LIBRO_ID)
WHERE ESTADO = 'activa';
  `)

module.exports = db;